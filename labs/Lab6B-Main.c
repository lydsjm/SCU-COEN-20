int main(void)
    {
    uint32_t color ;
    FILL f, *p ;
    QUEUE q ;

    InitializeHardware(HEADER, "Lab 6B: Recursive Flood Fill") ;
    InitializeTouchScreen() ;

    PaintImage(cat_bmp) ;
    ColorPicker() ;
    SanityCheck() ;

    f.x = f.y = 0 ;
    f.old = COLOR_WHITE ;
    f.new = COLOR_GREEN ;

    q.index = q.count = 0 ;

    for (;;)
        {
        if (TS_Touched())
            {
            f.x = TS_GetX() ;
            f.y = TS_GetY() ;

            if (f.y > IMAGE_YMIN && f.y < IMAGE_YMAX)
                {
                if (f.x < PICKER_XMIN)
                    {
                    f.old = *PixelAdrs(f.x, f.y) ;
                    if (f.old != COLOR_BLACK && f.new != f.old)
                        {
                        FloodFill(f.x, f.y, f.old, f.new) ;
                        Push(&q, &f) ;
                        }
                    }
                else
                    {
                    color = *PixelAdrs(f.x, f.y) ;
                    if (color != COLOR_BLACK) f.new = color ;
                    }
                }
            do Delay(10) ;
            while (TS_Touched()) ;
            }
        else if (PushButtonPressed())
            {
            WaitForPushButton() ;
            p = Pop(&q) ;
            if (p != NULL) FloodFill(p->x, p->y, p->new, p->old) ;
            }
        }

    return 0 ;
    }

BOOL OutOfBounds(int32_t x, int32_t y)
    {
    if (x < IMAGE_XMIN || x > IMAGE_XMAX) return TRUE ;
    if (y < IMAGE_YMIN || y > IMAGE_YMAX) return TRUE ;
    return FALSE ;
    }

uint32_t *PixelAdrs(int32_t x, int32_t y)
    {
    return (uint32_t *) 0xD0000000 + x + 240*y ;
    }

static void Push(QUEUE *q, FILL *f)
    {
    q->index++ ;
    if (q->index == ENTRIES(q->fills)) q->index = 0 ;
    q->fills[q->index] = *f ;
    if (q->count < ENTRIES(q->fills)) q->count++ ;
    }

static FILL *Pop(QUEUE *q)
    {
    FILL *f ;

    if (q->count == 0) return NULL ;
    f = &q->fills[q->index] ;
    if (q->index == 0) q->index = ENTRIES(q->fills) ;
    q->index-- ;
    q->count-- ;
    return f ;
    }

static void PaintImage(const void *bmp)
    {
    int row, col, rows, cols, xoff, yoff, rlen ;
    BMP_HEADER *header = (BMP_HEADER *) bmp ;
    uint8_t *prow ;

    rows = header->height ;
    cols = header->width ;

    prow = (uint8_t *) header + header->offset ;
    rlen = (cols/8 + 3) & ~0x3  ;

    xoff = (XPIXELS - cols) / 2 ;
    yoff = IMAGE_YMIN ;

    for (row = 0; row < rows; row++)
        {
        for (col = 0; col < cols; col++)
            {
            uint32_t bit = (prow[col / 8] >> (7 - col % 8)) & 1 ;
            uint32_t color = bit ? COLOR_WHITE : COLOR_BLACK ;
            *PixelAdrs(xoff + col, yoff + rows - row) = color ;
            }
        prow += rlen ;
        }
    }

