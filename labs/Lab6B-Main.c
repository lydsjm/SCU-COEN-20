// Functions to be implemented in assembly:
void __attribute__((weak)) FloodFill(int32_t x, int32_t y, uint32_t old_color, uint32_t new_color) 
    {
    uint32_t *ppxl ;    // Use a register to hold this temporary variable

    if (OutOfBounds(x, y)) return ;

    ppxl = PixelAdrs(x, y) ;
    if (*ppxl!= old_color) return ;
    *ppxl = new_color ;

    FloodFill(x - 1, y, old_color, new_color) ;
    FloodFill(x + 1, y, old_color, new_color) ;
    FloodFill(x, y - 1, old_color, new_color) ;
    FloodFill(x, y + 1, old_color, new_color) ;
    }

