static uint32_t GetTimeout(uint32_t msec)
    {
    uint32_t cycles = 1000 * msec * CPU_CLOCK_SPEED_MHZ ;
    return GetClockCycleCount() + cycles ;
    }

static void Delay(uint32_t msec)
    {
    uint32_t timeout = GetTimeout(msec) ;
    while ((int) (timeout - GetClockCycleCount()) > 0) ;
    }

static void LEDs(int grn_on, int red_on)
    {
    static uint32_t * const pGPIOG_MODER    = (uint32_t *) 0x40021800 ;
    static uint32_t * const pGPIOG_ODR      = (uint32_t *) 0x40021814 ;
    
    *pGPIOG_MODER |= (1 << 28) | (1 << 26) ;    // output mode
    *pGPIOG_ODR &= ~(3 << 13) ;         // both off
    *pGPIOG_ODR |= (grn_on ? 1 : 0) << 13 ;
    *pGPIOG_ODR |= (red_on ? 1 : 0) << 14 ;
    }

static void SanityCheck(void)
    {
    uint32_t color ;
    int32_t x, y ;

    LEDs(FALSE, TRUE) ; // Turn on RED in case FloodFill doesn't return
    
    FloodFill(0, 0, 0, 0) ; // Test OutOfBounds checking
    FloodFill(0, IMAGE_YMIN, COLOR_RED, COLOR_BLACK) ;  // No color match

    x = PICKER_XMIN ;
    y = PICKER_YMIN + PICKER_HEIGHT ;
    color = *PixelAdrs(x + 1, y + 1) ;
    FloodFill(x + 1, y + 1, color, COLOR_GRAY) ;
    if (*PixelAdrs(x + PICKER_WIDTH/2, y + PICKER_HEIGHT/2) != COLOR_GRAY)
        {
        Error(" Filled wrong color ") ;
        }
    FloodFill(x + 1, y + 1, COLOR_GRAY, color) ;    // Restore color

    if (*PixelAdrs(x + PICKER_WIDTH/2, y) != COLOR_BLACK)
        {
        Error(" Filled above region ") ;
        } 

    if (*PixelAdrs(x + PICKER_WIDTH/2, y + PICKER_HEIGHT) != COLOR_BLACK)
        {
        Error(" Filled below region ") ;
        } 

    if (*PixelAdrs(x, y + PICKER_HEIGHT/2) != COLOR_BLACK)
        {
        Error(" Filled left of region ") ;
        } 

    LEDs(TRUE, FALSE) ;
    }

